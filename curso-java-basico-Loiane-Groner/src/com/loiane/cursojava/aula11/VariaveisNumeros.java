package com.loiane.cursojava.aula11;

public class VariaveisNumeros {

	
	public static void main(String[] args) {
		
		int decVal = 26;
		
		int hexVal = 0x1a;
		
		int octVal = 032;
		
		int binVal = 0b11010;
		
		long cpf = 308_170_438_37L;
				
		System.out.println ("decimal   "  + binVal);
				
		System.out.println ("hexadecimal   "  + decVal);
		
		System.out.println ("octal   "   + hexVal);
		
		System.out.println ("binário   "  + octVal);
		
		System.out.println ("teste cpf   "  + cpf);
	}
}
